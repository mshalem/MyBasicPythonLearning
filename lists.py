friends = ["kevin","karen","jim", "john","alex"]
friends[1]="mike" #you can replace the element in the list
print(friends[1])
print(friends[0])  #prints 0th index means first element
print(friends[2])  #prints 2nd index means third element
print (friends[1:]) #prints index 1 to end of the list
print (friends[1:4]) #prints index 1 to index 3 it does not print index 4
print (friends[-1]) #if we gives negative it prints in the reverse order
print (friends[-2:]) # the index of the last element is -1 and highest positive index.
print (friends[-3:1])